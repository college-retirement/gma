<b>Characteristics of an Auditory Learner</b>
<ul>
	<li>Likes to read to self out loud.</li>
	<li>Is not afraid to speak in class.</li>
	<li>Likes oral reports.</li>
	<li>Is good at explaining.</li>
	<li>Remembers names.</li>
	<li>Notices sound effects in movies.</li>
	<li>Enjoys music.</li>
	<li>Is good at grammar and foreign language.</li>
	<li>Reads slowly.</li>
	<li>Follows spoken directions well.</li>
	<li>Can't keep quiet for long periods.</li>
	<li>Enjoys acting, being on stage.</li>
	<li>Is good in study groups.</li>
</ul>
<br>
<b>Study Tips for Auditory Learners</b>
<ul>
	<li>Join a study group or work with a "study buddy" on an ongoing basis to review key information and prepare for exams.</li>
	<li>When studying by yourself, talk out loud to aid recall.</li>
	<li>Tape-record your lectures (with teacher/professor's permission).  Use the "Pause" button to avoid taping irrelevant information. Use a tape recorder equipped with a 3-digit counter.  At the beginning of each lecture, set your counter to "000".  If a concept discussed during lecture seems particularly confusing, glance at the counter number and jot it down in your notes.  Later, you can fast forward to that number to review the material that confused you during lecture.  Making use of a counter and pause button while tape recording allows you to avoid the tedious task of having to listen to hours and hours of lecture tape.</li>
	<li>Create your own audios by reading notes and textbook information into a digital recorder.  Use audio tapes for language practice.  When preparing for an exam, review the audios in your CD/MP3 player or whenever you can.</li>
	<li>When learning mathematical or technical information, "talk your way" through the new information.  State the problem in your own words.  Reason through solutions to problems by talking out loud to yourself or with a study partner.  To learn a sequence of steps, write them out in sentence form and read them out loud.</li>
	<li>Create musical jingles and mnemonics to aid memorization.</li>
	<li>You may want to sit near the side or back of the classroom where there is less visual stimulation.</li>
	<li>Repeat facts with eyes closed.</li>
	<li>VERBALIZE AS MUCH AS POSSIBLE!!!</li>
</ul>
<br>
<b>Best Test Types</b>
<br>
Auditory Learners are good at writing responses to lectures they've heard.  They're also good at oral exams.
<br><br>
<b>Worst Test Types</b>
<br>
Reading passages and writing answers about them in a timed test.
