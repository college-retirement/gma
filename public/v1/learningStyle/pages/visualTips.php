<b>Characteristics of a Visual Learner</b>
<ul>
	<li>Learns through seeing things.</li>
	<li>Is good at spelling but forgets names.</li>
	<li>Needs quiet study time.</li>
	<li>Has to think awhile before understanding lecture.</li>
	<li>Is good at spelling.</li>
	<li>Likes colors & fashion.</li>
	<li>Dreams in color.</li>
	<li>Understands/likes charts.</li>
	<li>Is good with sign language.</li>
</ul>
<br>
<b>Study Tips for Visual Learners</b>
<ul>
	<li>Draw a map of events in history or draw scientific process.</li>
	<li>Make outlines of everything!</li>
	<li>Copy what’s on the board.</li>
	<li>Ask the teacher to diagram.</li>
	<li>Diagram sentences.</li>
	<li>Make lists.</li>
	<li>Take notes.</li>
		<ul>
			<li>Study text, but experiment between handwritten notes and printed typed-out pages. Experiment with cursive and print on the handwritten side (unless you’re one of those people who struggle to read cursive). Those who prefer type will probably prefer a standard font like Times New Roman or Verdana, but you should probably at least test a non-standard font to make certain that isn't your mental preference.</li>
		</ul>
	<li>Watch videos.</li>
		<ul>
			<li>Find a film documentary or TV show talking about the subject, especially if you’re also an auditory learner. The films don’t even have to be professionally made; maybe you could find some short homemade ones online. Can’t find that? You could even try to make your own, if you have to, and have access to a camcorder, which would particularly help visual-kinesthetic learners.</li>
		</ul>
	<li>Maps.
		<ul>
			<li>Try to get a map when going places instead of written directions, too -- unless you like being lost, of course.</li>
		</ul>
	<li>Study off the computer screen.</li>
	<li>Color code words, research notes.</li>
		<ul>
			<li>Pictures - Are there pictures in your textbook or notes? Study them.  In areas that you don’t have pictures, find some, so you can study the pictures to help you remember. (Google image search can work well.)
		</ul>
	<li>Outline reading.</li>
	<li>Use flashcards.</li>
	<li>Use highlighters, circle words, underline.</li>
</ul>
<br>
You'll have to mix and match these suggestions to find what works best for you.  Finding the proper combination for you will take work.  But it'll be well worth it.
<br><br>
<b>Best Test Types</b>
<br>
Diagramming, reading maps, essays (if you've studied using an outline), showing a process
<br><br>
<b>Worst Test Types</b>
<br>
Listen and respond tests
