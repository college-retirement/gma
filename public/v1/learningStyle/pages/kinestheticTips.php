<div class="crsLarge" style="text-align:center;">Kinesthetic Learners</div>
<b>Characteristics of a Kinesthetic Learner</b>
<ul>
	<li>Is good at sports.</li>
	<li>Can't sit still for long.</li>
	<li>Is not great at spelling.</li>
	<li>Does not have great handwriting.</li>
	<li>Likes science lab.</li>
	<li>Studies with loud music on.</li>
	<li>Likes adventure books, movies.</li>
	<li>Likes role playing.</li>
	<li>Takes breaks when studying.</li>
	<li>Builds models.</li>
	<li>Is involved in martial arts, dance.</li>
	<li>Is fidgety during lectures.</li>
</ul>
<br>
<b>Study Tips for Kinesthetic Learners</b>
<ul>
	<li>Study in short blocks</li>
	<li>Take lab classes</li>
	<li>Role play</li>
	<li>Take field trips, visit museums</li>
	<li>Study with others</li>
	<li>Use memory games</li>
	<li>Use flash cards to memorize</li>
</ul>
<br>
<b>Best Test Types</b>
<br>
Short definitions, fill-ins, multiple choice
<br><br>
<b>Worst Test Types</b>
<br>
Long tests, essays
